package uk.co.ogauthority.pwa.model.form.documents;

public class ClauseForm {

  private String name;

  private String text;

  public ClauseForm() {
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getText() {
    return text;
  }

  public void setText(String text) {
    this.text = text;
  }
}
